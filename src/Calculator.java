public class Calculator {
    void add (int a, int b){
        System.out.println("wynik dodawania dwóch liczb to: " + (a + b));
    }

    void add (int a, int b, int c){
        System.out.println("wynik dodawania trzech liczb to: "
                + (a + b + c));
    }

    void sub (int a, int b){
        System.out.println("wynik odejmowania dwóch liczb to: " + (a - b));
    }

    void sub (int a, int b, int c){
        System.out.println("wynik odejmowania trzech liczb to: " +
                (a - b - c));
    }
}
